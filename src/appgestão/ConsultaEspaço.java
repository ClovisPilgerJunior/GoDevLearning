    public void PesquisaAluno(String paramBusca) {

        String SQLPesquisa = "SELECT * FROM espaço" + paramBusca;
        Statement stmt;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            String db = "jdbc:mysql://localhost/minhadb";
            String user = "root";
            String password = "root";
            conn = DriverManager.getConnection(db, user, password);
            stmt = conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,
                    ResultSet.CONCUR_READ_ONLY);
            rs = stmt.executeQuery(SQLPesquisa);
            DefaultTableModel model = (DefaultTableModel) tblEspaço.getModel();
            model.setNumRows(0);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("Nome"),
                    rs.getString("Lotação")
                });

            }
        } catch (ClassNotFoundException | SQLException e) {
            System.out.println(e);
        }
    }

